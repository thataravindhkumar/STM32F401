uint8_t RxData[100]; //Buffer to store the complete received data
uint8_t dataToSend[100] = "Hello from stm32f401rct6!\n\rSend any message and I will send it back :D\n\r"; //Buffer which stores welcome message as well as the data to be sent later
uint8_t RxBuffer[1]; //Buffer to store a single character to be written into the RxData buffer one by one
uint8_t position = 0; //Index to store characters in order into the RxData buffer
uint8_t isSent = 1; //Variable to check whether previous transmission is complete

void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) //Function prototype copied from stm32f4xx_hal_uart.h file, gets called when a character has been received
{
	//Go into this if statement only if the string has been completed (whether a linefeed or carriage return is sent at the end of a string) or if the buffer is full
	if(position>=100 || RxBuffer[0] == '\n' || RxBuffer[0] == '\r')
	{
		//This for loop will fill up the empty spaces of the RxData buffer with null characters
		for(; position<100; position++)
		{
			RxData[position] = '\0';
		}
		//Reset the index back to 0 for next string
		position = 0;

		//Copy the contents of RxData buffer to dataToSend buffer for transmitting it back
		for(uint8_t i = 0; i<100; i++)
		{
			dataToSend[i] = RxData[i];
		}

		//Transmit the contents of dataToSend over UART by checking if previous transmission is complete
		if(isSent)
		{
			//Set isSent to 0 to denote that current transmission is about to start and new transmission should not happen right now
			isSent = 0;
			//strlen is used to exclude null characters from being sent
			HAL_UART_Transmit_IT(&huart1, dataToSend, strlen((char*)dataToSend));
		}
	}

	//This statement executes until the end of the string or RxData buffer becomes full, fills up RxData buffer with the received string one character at a time
	else
	{
		RxData[position] = RxBuffer[0];
		position++;
	}
	HAL_UART_Receive_IT(&huart1, RxBuffer, 1); //Be ready to receive the next character
}

void HAL_UART_TxCpltCallback(UART_HandleTypeDef *huart)
{
	//Set isSent to 1 to denote that previous transmission has completed and new transmission can be done
	isSent = 1;
}

